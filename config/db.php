<?php
$host = 'localhost';
$db = 'lucky'; // lina ism data_base fi phpmyadmin
$user = 'root'; // Your database username
$pass = ''; // Your database password

try {
    $pdo = new PDO("mysql:host=$host;dbname=$db", $user, $pass);
